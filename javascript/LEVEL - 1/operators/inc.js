let count = 1;

// increment -> post & pre

count++; //2

console.log(count);//2

console.log(count++);//2

console.log(count);//3

console.log(++count);//4

console.log('---------------')
// decrement -> post & pre  

console.log(count); // 4

console.log(count--); //4

console.log(count); // 3

console.log(--count); // 2
